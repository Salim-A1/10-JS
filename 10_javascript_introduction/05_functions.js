// Funktionsdifnition
function sum(x, y) {
    let ergebnis = x + y;
    return ergebnis; // Schlüssel für Ausgabewert
}
// funktionsaufruf
sum(10, 2);
sum(12, 3);
console.log("Ergebnis = " + sum(2, 3));

